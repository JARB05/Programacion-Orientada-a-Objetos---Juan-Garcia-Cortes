package edu.juan.garcia.evidencia2.ui;

/**
 * Clase abstracta que proporciona cadenas de texto para la interfaz de usuario de la aplicación TecmiFlix.
 * Las subclases de esta clase deben proporcionar implementaciones específicas para diferentes idiomas.
 */
public abstract class Textos {

    /** Título de la aplicación */
    public static String TITULO;

    /** Mensaje para solicitar el nombre de usuario */
    public static String USUARIO;

    /** Mensaje para solicitar la contraseña */
    public static String CONTRASENA;

    /** Mensaje que indica el costo total */
    public static String COSTO;

    /** Mensaje para solicitar si se desea ver el desglose de las compras */
    public static String DESGLOSEP;

    /** Mensaje que muestra el desglose de las compras */
    public static String DESGLOSE;

    /** Mensaje para indicar que la opción ingresada no es válida */
    public static String NOVALIDO;
}